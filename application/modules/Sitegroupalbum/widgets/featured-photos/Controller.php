class Sitegroupalbum_Widget_FeaturedPhotosController extends Engine_Content_Widget_Abstract {

  public function indexAction() {

    $category_id = $this->_getParam('category_id',0);
    // Get paginator
    $parentTable = Engine_Api::_()->getItemTable('sitegroup_album');
    $parentTableName = $parentTable->info('name');
    $tableGroup = Engine_Api::_()->getDbtable('groups', 'sitegroup');
    $tableGroupName = $tableGroup->info('name');
    $table = Engine_Api::_()->getItemTable('sitegroup_photo');
    $tableName = $table->info('name');
    $select = $table->select()
                    ->setIntegrityCheck(false)
                    ->from($tableName);
    if (!Engine_Api::_()->sitegroup()->isLessThan417AlbumModule()) {
      $select->joinLeft($parentTableName, $parentTableName . '.album_id=' . $tableName . '.album_id', array('title AS album_title'));
    } else {
      $select->joinLeft($parentTableName, $parentTableName . '.album_id=' . $tableName . '.collection_id', array('title AS album_title'));
    }
    $select->joinLeft($tableGroupName, "$tableGroupName.group_id = $tableName.group_id", array('title AS group_title', 'photo_id as group_photo_id'));
    $select ->where($tableName . '.featured = ?', 1)
            ->order('Rand()');

    $select = $select
              ->where($tableGroupName . '.closed = ?', '0')
              ->where($tableGroupName . '.approved = ?', '1')
              ->where($tableGroupName . '.declined = ?', '0')
              ->where($tableGroupName . '.draft = ?', '1');

    if(!empty($category_id)) {
      $select->where($tableGroupName . '.	category_id =?', $category_id);
    }
    
    if(!Engine_Api::_()->getApi('settings', 'core')->getSetting('sitegroupalbum.hide.autogenerated', 1) ) {
			$select->where($parentTableName. '.default_value'.'= ?', 0);
			$select->where($parentTableName . ".type is Null");
    }     
    
    $this->view->paginator = $paginator = Zend_Paginator::factory($select);
		
    // Set item count per group and current group number
    $paginator->setItemCountPerPage($this->_getParam('itemCountPerGroup', 4));
    $paginator->setCurrentPageNumber($this->_getParam('group', 1));


    // Do not render if nothing to show
    if (($paginator->getTotalItemCount() <= 0)) {
      return $this->setNoRender();
    }
    $this->view->showLightBox = Engine_Api::_()->sitegroup()->canShowPhotoLightBox();
    if ($this->view->showLightBox) {
      $this->view->params = $params = array('type' => 'featured', 'count' => $paginator->getTotalItemCount(), 'title' => $this->_getParam('title', 'Featured Photos'));
    }
  }

}
